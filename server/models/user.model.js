import mongoose from 'mongoose';

const userSchema = new mongoose.Schema(
    {
        username: {
            type: String,
            required: true,
            unique: true,
            trim: true,
        },
        email: {
            type: String,
            required: true,
            unique: true,
            trim: true,
        },
        password: {
            type: String,
            required: true,
            minlength: 6,
        },
        profilePic: {
            type: String,
            default: '',
        },
        // We'll manage online status mostly via socket, but can keep it here if needed
    },
    { timestamps: true }
);

const User = mongoose.model('User', userSchema);
export default User;
